const supportedLanguages = ["en", "ja", "zh-Hans", "zh-Hant"];

function normalizeLanguage(value) {
  if (!value) return null;
  const lowered = value.toLowerCase();
  if (lowered.startsWith("ja")) return "ja";
  if (lowered.startsWith("zh-hant") || lowered.startsWith("zh-tw") || lowered.startsWith("zh-hk")) return "zh-Hant";
  if (lowered.startsWith("zh")) return "zh-Hans";
  if (lowered.startsWith("en")) return "en";
  return null;
}

function selectedLanguage() {
  const query = normalizeLanguage(new URLSearchParams(window.location.search).get("lang"));
  const stored = normalizeLanguage(window.localStorage.getItem("tsumo-language"));
  const browser = normalizeLanguage(navigator.language);
  return query || stored || browser || "en";
}

function applyLanguage(language) {
  const selected = supportedLanguages.includes(language) ? language : "en";
  const dictionary = translations[selected];

  document.documentElement.lang = selected;
  document.querySelectorAll("[data-i18n]").forEach((element) => {
    const value = dictionary[element.dataset.i18n];
    if (value) element.textContent = value;
  });
  document.querySelectorAll("[data-i18n-content]").forEach((element) => {
    const value = dictionary[element.dataset.i18nContent];
    if (value) element.setAttribute("content", value);
  });

  const page = document.body.dataset.page;
  if (page && dictionary[`${page}.title`]) {
    document.title = `${dictionary[`${page}.title`]} · ${dictionary["site.name"]}`;
  }

  const select = document.querySelector("#language-select");
  if (select) {
    select.value = selected;
    select.setAttribute("aria-label", dictionary["nav.language"]);
  }

  document.querySelectorAll("a[data-language-link]").forEach((link) => {
    const url = new URL(link.href, window.location.href);
    url.searchParams.set("lang", selected);
    link.href = url.toString();
  });

  window.localStorage.setItem("tsumo-language", selected);
}

document.addEventListener("DOMContentLoaded", () => {
  applyLanguage(selectedLanguage());
  const select = document.querySelector("#language-select");
  select?.addEventListener("change", (event) => {
    const language = event.target.value;
    const url = new URL(window.location.href);
    url.searchParams.set("lang", language);
    window.history.replaceState({}, "", url);
    applyLanguage(language);
  });
});
